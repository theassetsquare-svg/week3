function josa(word, pair) {
  var p = pair.split("/");
  var c = word.charCodeAt(word.length - 1);
  if (c >= 0xAC00 && c <= 0xD7A3) return (c - 0xAC00) % 28 !== 0 ? p[0] : p[1];
  return p[0];
}
